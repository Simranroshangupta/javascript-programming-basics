// ArrayCreation.js
// Demonstrates array creation in JavaScript.

// Creating an array of fruits
const fruits = ["Apple", "Banana", "Mango", "Orange"];

// Displaying the entire array
console.log("Fruits :", fruits);

// Accessing array elements
console.log("First Fruit :", fruits[0]);
console.log("Second Fruit :", fruits[1]);
console.log("Third Fruit :", fruits[2]);
console.log("Fourth Fruit :", fruits[3]);

/*
==========================================
Explanation

An array is a collection of
multiple values stored in a
single variable.

Each element has an index.

Index starts from 0.

Example

Index : 0  1  2  3
Value : Apple Banana Mango Orange

When to Use

Use arrays when you need
to store multiple values
of the same type.

Expected Output

Fruits : [ 'Apple', 'Banana', 'Mango', 'Orange' ]
First Fruit : Apple
Second Fruit : Banana
Third Fruit : Mango
Fourth Fruit : Orange

Time Complexity

Access Element : O(1)

Space Complexity

O(n)

==========================================
*/