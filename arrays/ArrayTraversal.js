// ArrayTraversal.js
// Demonstrates array traversal in JavaScript.

const numbers = [10, 20, 30, 40, 50];

for (let index = 0; index < numbers.length; index++) {

    console.log(numbers[index]);
}

/*
==========================================
Explanation

Traversal means visiting
every element of an array.

The for loop starts from
index 0 and continues until
the last element.

numbers.length returns
the total number of elements.

When to Use

Use array traversal whenever
you need to process every
element of an array.

Expected Output

10
20
30
40
50

Time Complexity

O(n)

Space Complexity

O(1)

==========================================
*/