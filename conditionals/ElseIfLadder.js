// ElseIfLadder.js
// Demonstrates the else if ladder.

let marks = 78;

if (marks >= 90) {

    console.log("Grade A");

} else if (marks >= 75) {

    console.log("Grade B");

} else if (marks >= 50) {

    console.log("Grade C");

} else {

    console.log("Fail");
}

/*
==========================================
Explanation

The else if ladder is used
when multiple conditions
need to be checked.

The first condition that
evaluates to true is executed.

Expected Output

Grade B

==========================================
*/