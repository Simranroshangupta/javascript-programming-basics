// IfElseStatement.js
// Demonstrates the if...else statement.

let number = 15;

if (number % 2 === 0) {

    console.log(number + " is Even.");

} else {

    console.log(number + " is Odd.");
}

/*
==========================================
Explanation

The if...else statement
checks a condition.

If the condition is true,
the if block executes.

Otherwise,
the else block executes.

Expected Output

15 is Odd.

==========================================
*/