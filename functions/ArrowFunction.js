// ArrowFunction.js
// Demonstrates arrow functions in JavaScript.

const square = (number) => {

    return number * number;
};

console.log("Square :", square(5));

/*
==========================================
Explanation

Arrow functions provide a
shorter syntax for writing
functions.

They were introduced in ES6.

When to Use

Use arrow functions for
short and simple functions.

Expected Output

Square : 25

==========================================
*/