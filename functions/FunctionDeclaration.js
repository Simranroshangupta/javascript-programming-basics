// FunctionDeclaration.js
// Demonstrates function declaration in JavaScript.

function greetUser(name) {

    console.log("Welcome,", name + "!");
}

greetUser("Simran");

/*
==========================================
Explanation

A function is a reusable block
of code that performs a
specific task.

A function declaration starts
with the 'function' keyword,
followed by the function name
and parameters.

When to Use

Use function declarations
when you want a reusable
piece of code.

Expected Output

Welcome, Simran!

==========================================
*/