// FunctionExpression.js
// Demonstrates function expressions in JavaScript.

const greetUser = function (name) {

    console.log("Hello,", name + "!");
};

greetUser("Simran");

/*
==========================================
Explanation

A function expression stores
a function inside a variable.

The function can then be
called using the variable name.

When to Use

Use function expressions when
you want to assign a function
to a variable or pass it
as an argument.

Expected Output

Hello, Simran!

==========================================
*/