// ForOfLoop.js
// Demonstrates the for...of loop.

const fruits = ["Apple", "Banana", "Mango", "Orange"];

for (const fruit of fruits) {

    console.log(fruit);
}

/*
==========================================
Explanation

The for...of loop is used
to iterate over iterable
objects like arrays and strings.

It gives direct access
to each element.

When to Use

Use for...of when you
need to access the values
of an array or string.

Expected Output

Apple
Banana
Mango
Orange

==========================================
*/