// WhileLoop.js
// Demonstrates the while loop in JavaScript.

let number = 1;

while (number <= 5) {

    console.log("Number :", number);

    number++;
}

/*
==========================================
Explanation

A while loop executes a block
of code as long as the given
condition is true.

The condition is checked
before each iteration.

Syntax

while (condition) {
    // Code
}

When to Use

Use a while loop when you
do not know in advance how
many times the loop should
execute.

Expected Output

Number : 1
Number : 2
Number : 3
Number : 4
Number : 5

==========================================
*/