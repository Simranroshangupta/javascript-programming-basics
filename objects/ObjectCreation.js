// ObjectCreation.js
// Demonstrates object creation in JavaScript.

const student = {

    name: "Simran",
    age: 20,
    course: "AIML"
};

console.log("Student Details");

console.log("Name :", student.name);
console.log("Age :", student.age);
console.log("Course :", student.course);

/*
==========================================
Explanation

An object stores data in the
form of key-value pairs.

Keys are called properties.

Objects are used to represent
real-world entities.

When to Use

Use objects to store related
information together.

Expected Output

Student Details
Name : Simran
Age : 20
Course : AIML

Time Complexity

O(1)

Space Complexity

O(1)

==========================================
*/