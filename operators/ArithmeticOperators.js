// ArithmeticOperators.js
// Demonstrates arithmetic operators in JavaScript.

let number1 = 20;
let number2 = 10;

console.log("Addition :", number1 + number2);
console.log("Subtraction :", number1 - number2);
console.log("Multiplication :", number1 * number2);
console.log("Division :", number1 / number2);
console.log("Modulus :", number1 % number2);
console.log("Exponent :", number1 ** 2);

/*
==========================================
Explanation

Arithmetic operators are used
to perform mathematical operations.

+   Addition
-   Subtraction
*   Multiplication
/   Division
%   Modulus (Remainder)
**  Exponent (Power)

Expected Output

Addition : 30
Subtraction : 10
Multiplication : 200
Division : 2
Modulus : 0
Exponent : 400

==========================================
*/