// AssignmentOperators.js
// Demonstrates assignment operators in JavaScript.

let number = 10;

console.log("Initial Value :", number);

number += 5;
console.log("After += :", number);

number -= 3;
console.log("After -= :", number);

number *= 2;
console.log("After *= :", number);

number /= 4;
console.log("After /= :", number);

number %= 3;
console.log("After %= :", number);

/*
==========================================
Explanation

Assignment operators assign
or update a variable.

=   Assign value
+=  Add and assign
-=  Subtract and assign
*=  Multiply and assign
/=  Divide and assign
%=  Modulus and assign

==========================================
*/