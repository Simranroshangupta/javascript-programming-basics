// ComparisonOperators.js
// Demonstrates comparison operators in JavaScript.

let number1 = 15;
let number2 = 20;

console.log(number1 == number2);
console.log(number1 != number2);
console.log(number1 > number2);
console.log(number1 < number2);
console.log(number1 >= number2);
console.log(number1 <= number2);
console.log(number1 === number2);
console.log(number1 !== number2);

/*
==========================================
Explanation

Comparison operators compare
two values and return either
true or false.

==   Equal
!=   Not Equal
>    Greater Than
<    Less Than
>=   Greater Than or Equal
<=   Less Than or Equal
===  Strict Equal
!==  Strict Not Equal

==========================================
*/