// IncrementDecrement.js
// Demonstrates increment and decrement operators.

let number = 10;

console.log("Original Value :", number);

number++;
console.log("After Increment :", number);

number--;
console.log("After Decrement :", number);

/*
==========================================
Explanation

Increment (++)
increases a value by 1.

Decrement (--)
decreases a value by 1.

These operators are commonly
used in loops and counters.

==========================================
*/