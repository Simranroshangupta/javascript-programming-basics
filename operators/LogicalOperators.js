// LogicalOperators.js
// Demonstrates logical operators in JavaScript.

let isStudent = true;
let hasID = false;

console.log("AND :", isStudent && hasID);
console.log("OR :", isStudent || hasID);
console.log("NOT :", !isStudent);

/*
==========================================
Explanation

Logical operators combine
multiple conditions.

&&  AND
Returns true if both
conditions are true.

||  OR
Returns true if at least
one condition is true.

!   NOT
Reverses the boolean value.

==========================================
*/