// TernaryOperator.js
// Demonstrates the ternary operator.

let age = 18;

let message = age >= 18
    ? "Eligible to Vote"
    : "Not Eligible to Vote";

console.log(message);

/*
==========================================
Explanation

The ternary operator is a
short form of an if...else
statement.

Syntax

condition ? value1 : value2

If the condition is true,
the first value is returned.

Otherwise,
the second value is returned.

Expected Output

Eligible to Vote

==========================================
*/