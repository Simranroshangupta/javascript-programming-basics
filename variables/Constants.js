// Constants.js
// Demonstrates constants in JavaScript.

const PI = 3.14159;
const COUNTRY = "India";

console.log("Value of PI :", PI);
console.log("Country :", COUNTRY);

// PI = 3.14; // Error: Assignment to constant variable.

/*
==========================================
Explanation

A constant stores a value that
cannot be changed after it is
assigned.

The 'const' keyword is used
to declare constants.

Use constants for values that
should remain the same throughout
the program.

Expected Output

Value of PI : 3.14159
Country : India

==========================================
*/