// Variables.js
// Demonstrates variables in JavaScript.

let name = "Simran";
let age = 20;
let city = "Delhi";

console.log("Name :", name);
console.log("Age :", age);
console.log("City :", city);

/*
==========================================
Explanation

A variable is used to store data.

The 'let' keyword is used to
declare a variable whose value
can be changed later.

Use meaningful variable names
to make your code easy to read.

Expected Output

Name : Simran
Age : 20
City : Delhi

==========================================
*/